    private void btnsubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsubmitActionPerformed
        
        
        if(txtpname.getText().equals(""))
        {
            txtpname.requestFocusInWindow();
            JOptionPane.showMessageDialog(null, "Enter Full name");
        }
        else if(txtpmobileno.getText().equals(""))
        {
            txtpmobileno.requestFocusInWindow();
            JOptionPane.showMessageDialog(null, "Enter Mobile Number");
        }
        else if(txtpmobileno.getText().length()!=10)
        {
            txtpmobileno.setText("");
            txtpmobileno.requestFocusInWindow();
            JOptionPane.showMessageDialog(null, "Enter 10 digit number");
        }
        else if(txtpemailid.getText().equals(""))
        {
            txtpemailid.requestFocusInWindow();
            JOptionPane.showMessageDialog(null, "Enter Email ID");
        } 
        else if(txtperaddress.getText().equals(""))
        {
            txtperaddress.requestFocusInWindow();
            JOptionPane.showMessageDialog(null, "Enter Address");
        }
        else if(txtlocaddress.getText().equals(""))
        {
            txtlocaddress.requestFocusInWindow();
            JOptionPane.showMessageDialog(null, "Enter Address");
        }
        else if(cmbcity.getSelectedIndex()==0)
        {
            JOptionPane.showMessageDialog(null, "Select city name");
        }
        else{
            try {                
                Connection con;
                con = DriverManager.getConnection("jdbc:mysql://localhost:3306/enquiryformtest", "root", "root");
                Statement st = con.createStatement();            
                String query2="insert into mainform(id,fname,mname,lname,phone,mail,birthdate,gender,clgid,branchid,shift,courseid,fathername,p_phone,p_mail,peraddress,locaddress,cityid)"
                    + " values("+id+",'"+fname+"','"+mname+"','"+lname+"','"+phone+"','"+mail+"','"+birthdate+"','"+gender+"',"+clgid+","+branchid+","+shift+","+courseid+",'"+txtpname.getText() + "','" + txtpmobileno.getText() + "','" + txtpemailid.getText() + "','" + txtperaddress.getText() + "','" + txtlocaddress.getText() + "'," + cmbcity.getSelectedIndex() +")";               
                st.executeUpdate(query2);
                JOptionPane.showMessageDialog(null, "Successfully Entered");
                page1 pg1=new page1();
                pg1.setVisible(true);
                dispose();
                pg2.dispose();
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }//GEN-LAST:event_btnsubmitActionPerformed

    private void txtpnameKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtpnameKeyTyped
        char ch;
        ch = evt.getKeyChar();
        if (Character.isAlphabetic(ch) != true && Character.isAlphabetic(evt.VK_SPACE)) {
            evt.consume();
        }
    }//GEN-LAST:event_txtpnameKeyTyped


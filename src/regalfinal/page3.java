    private void Fillcmbcityname() {
        try {
            Connection con;
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/enquiryformtest", "root", "root");
            String sql1 = "select cityname from city";
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery(sql1);
            while (rs.next()) {
                String name = rs.getString("cityname");
                cmbcity.addItem(name);
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

